public class SaladModel {
    public int imageId;
    public String txt;
    public String id;

    public void setImageId(int imageId) {
        this.imageId = imageId;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getImageId() {
        return imageId;
    }

    public String getTxt() {
        return txt;
    }

    public String getId() {
        return id;
    }
}
